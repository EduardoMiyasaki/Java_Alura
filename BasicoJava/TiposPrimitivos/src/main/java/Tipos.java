public class Tipos {

    public static void main(String[] args) {

        // Tipos númericos

        byte bytes = 127;
        short short1 = 32767;
        int inteiro = 2147483647;
        long longo = 9223372036854775807L;

        // valores máximos de cada tipo

        float flutuante = 12.2F;
        double dobro = 12.2;

        // sempre tem que colocar em aspas simples
        char caractere = 'd';

        boolean falso = true;

        String nome = "Eduardo";

        System.out.println(nome);
    }
}
